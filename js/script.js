function escolherOpcao(opcao) {
  if (opcao === "paciente") {
    alert("Você escolheu criar conta como Paciente!");
  } else if (opcao === "medico") {
    alert("Você escolheu criar conta como Médico!");
  } else if (opcao === "login") {
    alert("Você escolheu entrar em uma conta existente!");
  }
}
